package com.piatakha;

public class che {

	// Символьные переменные ведут себя подобно целочисленным значениям,
	public static void main(String args[]) {
	char chi;
	chi = 'X';
	System.out.println("chi содержит " + chi);
	chi++; // увеличение значения chi на единицу
	System.out.println("chi теперь " + chi);
	}
}
